package basic;
public class constructor {
    public static void main(String[] args) {
        /**
         *   构造器
         *   1） 方法名和类名相同
         *   2） 没有返回值
         *   3） 在创建对象时，系统自动调用该类的构造器完成初始化
         * */

        /**
         *  1.一个类可以定义多个不同的构造器，即构造器重载
         *  2.构造器名和类名要相同
         *  3构造器没有返回值
         *  4.构造器完成对象初始化，不是创建对象
         *  5.如果自己没有定义构造器，系统会自动给类生成一个默认无参构造方法。
         *  6.一旦定义了自己的构造器，默认的构造器就被覆盖了，除非显示的定义一下， xxx(){}
         *  */
    }
}
