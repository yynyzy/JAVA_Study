public class Array_ {
    public static void main(String[] args) {
        // # 数组的三种声明方式
        double a1[] = new double[5];

        double a2[];
        a2 = new double[5];

        //静态声明
        double a3[] = {1, 2, 3, 4, 5};

        // # 数组创建后后的默认值
        /*
        *   int 0, short 0, byte 0, long 0,
        *  float 0.0, double 0.0, char \u0000,
        *  boolean false, String null
        * */
    }
}
